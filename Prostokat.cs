using System;
namespace Application
{
    public class Prostokat
    {

        public static double Pole(double bok1, double bok2)
        {
            return bok1 * bok2;
        }
        public static double Obwod(double bok1, double bok2)
        {
            return (2*bok1) + (2*bok2);
        }
    }
}
